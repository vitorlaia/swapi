package swapi.obj;

/**
 *
 * @author Vitor
 */
public class Travel {
int mglt;
String qtyParada;
String name;
int passengers;

    public int getMglt() {
        return mglt;
    }

    public void setMglt(int mglt) {
        this.mglt = mglt;
    }

    public String getQtyParada() {
        return qtyParada;
    }

    public void setQtyParada(String qtyParada) {
        this.qtyParada = qtyParada;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPassangers() {
        return passengers;
    }

    public void setPassangers(int passangers) {
        this.passengers = passangers;
    }

}
